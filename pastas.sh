# bin - onde ficam os executáveis
# boot - pasta usada para iniciar o sistema
# dev - representação das unidade dos discos
# etc - configurações dos sistema para todos os usuários
# home - ficam as pastas dos usuários
# lib's - onde ficam as libs que programas usam
# media - ficam as montagens automaticas
# mnt - onde ficam as montagens feitas manualmente 
# opt - geralmente onde fabricantes colocam softwares proprietários
# proc - diretório virtual que são criado no boot do sistema. Cada pasta representa um processo do sistema
# root - home para root
# run - diretório virtual, é apagado quando o sistema é desligado
# sbin - binários que somente o sudo pode acessar
# srv - pastas de serviços
# sys - pastas para acessar o kernel
# tmp - arquivos temporários
# usr - onde progrmadas instalados via código font ficam e outros progrmas não excencias para o funcionamento do sistema
# var - logs, backups